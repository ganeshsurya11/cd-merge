class Manifestation < ActiveRecord::Base
  belongs_to :manifestation_type,  :inverse_of => :manifestations
  has_many :expressions, through: :expressions_has_manifestations
  has_many :manifestation_events,  :inverse_of => :manifestations
  has_many :items, :inverse_of => :manifestations
  has_many :digital_editions, :inverse_of => :manifestations
  attr_accessible :id, :manifestation_description, :manifestation_name, :manifestation_notes, :manifestation_siglum, :manifestation_type_id
end
